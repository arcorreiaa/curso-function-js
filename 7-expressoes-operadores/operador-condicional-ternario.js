// operador condicional ternario

// dependendo da condição, nós recebemos valores diferentes

// condição então valor 1 se não valor 2

// condition ? value1 : value2

// exemplos

// café da manhã top
let pao = true;
let queijo = true;

const cafeBom = pao && queijo ? "Café gostoso em!" : "Café ruim, gosto não.";
console.log(cafeBom);

// maior que 18

let age = 21;

const dirigir = age >= 18 ? "Pode dirigit" : "não pode dirigir";

console.log(dirigir);
