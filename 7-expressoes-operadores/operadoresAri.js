// operadores aritimeticos

// multiplicação é o simbolo *

console.log(8 * 5.9);

// divisão é o simbolo /

console.log(12 / 3);

// soma é o simbolo +

console.log(22 + 5);

// subtração é o simbolo -

console.log(54 - 12);

// resto da divisão é o simbolo %

let restoDivisao;
restoDivisao = 37 % 8;
console.log(restoDivisao);

// incremento é o simbolo ++

let increment = 0; //supor que ele inicia com 0.
increment++;
console.log(increment);

// decremento é o simbolo -- e funciona ao contrário do incremento

let decrement = 10;
decrement--;
console.log(decrement);

// exponencial é o simbolo ** que é o numero elevado a outro numero

console.log(9 ** 5);
